#include <iostream>
using namespace std;

// Function to print each word on a new line
void printNewLine() {
    cout << "Geeks" << endl;  // Print "Geeks" and move to the next line
    cout << "for" << endl;    // Print "for" and move to the next line
    cout << "Geeks" << endl;  // Print "Geeks" and move to the next line
}

int main() {
    printNewLine();  // Call the function to print the words

    return 0;  // Return 0 to indicate successful execution
}


